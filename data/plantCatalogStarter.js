function normalizeLatinCatalogKey(value) {
  return String(value ?? '')
    .trim()
    .toLowerCase()
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/\s+/g, ' ');
}

function buildCatalogId(prefix, latinName) {
  const normalized = normalizeLatinCatalogKey(latinName)
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '');
  return `${prefix}_${normalized || 'unknown'}`;
}

