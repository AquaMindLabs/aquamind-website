function normalizeInlineText(value) {
  return String(value ?? '').replace(/\s+/g, ' ').trim();
}

function buildInlineTranslationMap(locale) {
  const normalizedLocale = String(locale ?? 'pl').toLowerCase();
  if (normalizedLocale === 'pl') {
    return new Map();
  }

  const bundle = TRANSLATIONS[normalizedLocale] ?? {};
  const map = new Map();
  Object.entries(TRANSLATIONS.pl ?? {}).forEach(([key, value]) => {
    if (typeof value !== 'string' || typeof bundle[key] !== 'string') {
      return;
    }
    map.set(normalizeInlineText(value), bundle[key]);
  });

  Object.entries(EXTRA_INLINE_TRANSLATIONS[normalizedLocale] ?? {}).forEach(
    ([source, translation]) => {
      map.set(normalizeInlineText(source), translation);
    }
  );
  return map;
}

const INLINE_MAP_CACHE = new Map();

export function translateInlineText(value, locale) {
  if (typeof value !== 'string') {
    return value;
  }
  const normalizedLocale = String(locale ?? 'pl').toLowerCase();
  if (normalizedLocale === 'pl') {
    return value;
  }

  const normalized = normalizeInlineText(value);
  if (!normalized) {
    return value;
  }
  if (!INLINE_MAP_CACHE.has(normalizedLocale)) {
    INLINE_MAP_CACHE.set(normalizedLocale, buildInlineTranslationMap(normalizedLocale));
  }

  const translated = INLINE_MAP_CACHE.get(normalizedLocale)?.get(normalized);
  if (!translated) {
    return value;
  }

  const leading = value.match(/^\s*/)?.[0] ?? '';
  const trailing = value.match(/\s*$/)?.[0] ?? '';
  return `${leading}${translated}${trailing}`;
}

export function translateInlineNode(node, locale) {
  if (typeof node === 'string') {
    return translateInlineText(node, locale);
  }
  if (Array.isArray(node)) {
    return node.map((child) => translateInlineNode(child, locale));
  }
  return node;
}
